						<h2><a href="#">Inštalácia TechnicoCraft Lite</a></h2>
						<span class="byline">
							Tento tútoriál je zameraný na použitie MultiMC5. Môžeťe ho získať tu: <strong><a href="http://files.multimc.org/testbuilds/mmc-win32.zip">WINDOWS</a></strong> | <strong><a href="http://files.multimc.org/testbuilds/mmc-lin64.tar.gz">LINUX</a></strong>. After you have extracted MultiMC zip file contents to a fresh folder, you are ready to go.
						</span>
					</header>
					<p>Start MultiMC.exe.  Click "Open The Instance Folder" in the upper-left corner.  Extract the downloaded .zip file into the instance folder.</p>
                    <p>Close the instance folder, click "Refresh" at the top, and you should see ForgeCraft 3!</p>
					<p>Recommended: Give your Minecraft instance a little more RAM and PermGen space (We recommend allocating at least 1024M of RAM and setting PermGen to at least 128). Click "Settings" and go into the "Java" tab.  Here you can increase your allocations :)</p>
					<p>Note: If you allocate over 2048M of RAM you will probably need to have 64-bit Java installed and configured in the MultiMC settings.</p>
